#!/bin/bash

# Скрипт для исправления кодировки файлов Pawn (Windows-1251 -> UTF-8)
# Запускать в папке с проектом или указать путь к папке как аргумент

echo "=== Fix Pawn Encoding Script ==="
echo "Поиск файлов .pwn, .inc и конфигурационных файлов..."

TARGET_DIR="${1:-.}"

if [ ! -d "$TARGET_DIR" ]; then
    echo "Ошибка: Директория $TARGET_DIR не найдена."
    exit 1
fi

# Проверка наличия утилиты iconv
if ! command -v iconv &> /dev/null; then
    echo "Ошибка: Утилита 'iconv' не найдена. Пожалуйста, установите её (например: apt-get install iconv)."
    exit 1
fi

count=0
fixed=0

# Поиск файлов
while IFS= read -r -d '' file; do
    count=$((count + 1))
    
    # Проверяем, является ли файл действительно в CP1251 (или содержит кракозябры)
    # Пытаемся сконвертировать. Если файл уже UTF-8, iconv может выдать ошибку или оставить как есть, 
    # но мы форсируем проверку через содержимое.
    
    # Создаем временный файл
    temp_file=$(mktemp)
    
    # Попытка конвертации из CP1251 в UTF-8
    # Если файл уже UTF-8, эта команда может испортить его, поэтому добавим проверку на типичные символы кириллицы в байтах CP1251
    # Но самый надежный способ для пользователей SA-MMP — просто пересохранить всё в UTF-8 без BOM
    
    if iconv -f WINDOWS-1251 -t UTF-8//IGNORE "$file" > "$temp_file" 2>/dev/null; then
        # Проверяем, изменилось ли содержимое (файл не пуст и конвертация прошла)
        if [ -s "$temp_file" ]; then
             # Дополнительная проверка: если в исходном файле были специфические байты CP1251
             # Для простоты в рамках фикса заменяем файл, если конвертация успешна
             mv "$temp_file" "$file"
             echo "[OK] Исправлено: $file"
             fixed=$((fixed + 1))
        else
            rm "$temp_file"
        fi
    else
        rm "$temp_file"
        # Файл мог быть уже в UTF-8 или битым, пропускаем с предупреждением, если ошибка не в кодировке
        :
    fi
done < <(find "$TARGET_DIR" -type f \( -name "*.pwn" -o -name "*.inc" -o -name "*.cfg" -o -name "*.txt" \) -print0)

echo "================================="
echo "Обработано файлов: $count"
echo "Исправлено (конвертировано): $fixed"
echo "Готово! Теперь попробуйте скомпилировать проект снова."
echo "Если ошибки остались, убедитесь, что в коде нет лишних скобок из-за поврежденных комментариев."
